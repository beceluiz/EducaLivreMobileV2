package com.example.educalivre_mobilev2;

public class Metadata {
    private int limit;
    private int offset;
    private int total;
    private boolean hasMore;

    public int getLimit() {
        return limit;
    }

    public int getOffset() {
        return offset;
    }

    public int getTotal() {
        return total;
    }

    public boolean isHasMore() {
        return hasMore;
    }
}

